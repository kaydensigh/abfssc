// Generate src/content/codelists.generated.ts from the extracted prose catalogue
// abf/extracted/field_codelists.md (the day-one md→data task, design §08).
// Run: node scripts/build-codelists.mjs
//
// The 52 coded fields each ship a tooltip code-list: a bare code (digit/`x`/`9`)
// that expands to a phrase. Phrases may carry !-codes (and backticks), which the
// render engine resolves — so they are transcribed verbatim, never paraphrased.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");
const SRC = resolve(root, "abf/extracted/field_codelists.md");
const OUT = resolve(root, "src/content/codelists.generated.ts");

const md = readFileSync(SRC, "utf8");
const lines = md.split(/\r?\n/);

// `- \`code\` — phrase`  (em dash U+2014, surrounded by spaces)
const bulletRe = /^- `([^`]+)`\s+—\s+(.*)$/;
const headerRe = /^### `([^`]+)`\s*$/;

const fields = [];
let cur = null;
let promptLines = null;

for (const line of lines) {
  const h = headerRe.exec(line);
  if (h) {
    cur = { key: h[1], prompt: "", codes: [] };
    promptLines = [];
    fields.push(cur);
    continue;
  }
  if (!cur) continue;
  const b = bulletRe.exec(line);
  if (b) {
    cur.codes.push({ code: b[1], phrase: b[2] });
    promptLines = null; // prompt ends once bullets start
    continue;
  }
  if (promptLines && line.trim().length > 0) {
    promptLines.push(line.trim());
    cur.prompt = promptLines.join(" ");
  }
}

const totalCodes = fields.reduce((n, f) => n + f.codes.length, 0);

const banner = `// AUTO-GENERATED by scripts/build-codelists.mjs from
// abf/extracted/field_codelists.md — do not edit by hand.
// ${fields.length} coded fields / ${totalCodes} codes.
`;

const body =
  banner +
  `import type { CodeList } from "./codelists.ts";\n\n` +
  `export const CODE_LISTS: Record<string, CodeList> = ${toTs(fields)};\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body, "utf8");
console.log(`Wrote ${OUT}: ${fields.length} fields, ${totalCodes} codes.`);

function q(s) {
  return JSON.stringify(s);
}
function toTs(fs) {
  const entries = fs
    .map((f) => {
      const codes = f.codes.map((c) => `      { code: ${q(c.code)}, phrase: ${q(c.phrase)} }`).join(",\n");
      return `  ${q(f.key)}: {\n    prompt: ${q(f.prompt)},\n    codes: [\n${codes}\n    ],\n  }`;
    })
    .join(",\n");
  return `{\n${entries},\n}`;
}
